// Extracts the EMS Allure single-stroke cursive font (from the hersheytext
// package, SIL/free plotter fonts by Evil Mad Scientist) into a compact TS
// module. These fonts are polyline-only (M/L path commands), i.e. genuine pen
// strokes — exactly what "rewrite handwriting as calligraphy ink" needs.
//
// Run after changing the char set:  node scripts/gen-script-font.mjs

import { readFileSync, writeFileSync } from "fs";

const SRC = "node_modules/hersheytext/svg_fonts/EMSAllure.svg";

// Printable ASCII + German set (the user's lecture notes are German/English).
const WANTED = new Set();
for (let c = 32; c <= 126; c++) WANTED.add(String.fromCharCode(c));
for (const c of "äöüßÄÖÜéèêàç€„“”‚’–") WANTED.add(c);

const svg = readFileSync(SRC, "utf8");

const face = {
  unitsPerEm: Number(svg.match(/units-per-em="([^"]+)"/)?.[1] ?? 1000),
  capHeight: Number(svg.match(/cap-height="([^"]+)"/)?.[1] ?? 500),
  xHeight: Number(svg.match(/x-height="([^"]+)"/)?.[1] ?? 300),
  defaultAdv: Number(svg.match(/<font[^>]*horiz-adv-x="([^"]+)"/)?.[1] ?? 378),
};

function decodeEntity(u) {
  if (u.startsWith("&#x")) return String.fromCodePoint(parseInt(u.slice(3, -1), 16));
  if (u.startsWith("&#")) return String.fromCodePoint(parseInt(u.slice(2, -1), 10));
  const named = { "&amp;": "&", "&apos;": "'", "&quot;": '"', "&lt;": "<", "&gt;": ">" };
  return named[u] ?? u;
}

/** Parse "M x y L x y …" into polylines, rounding to 1 decimal. */
function parsePath(d) {
  const lines = [];
  let current = null;
  const tokens = d.match(/[ML]|-?[\d.]+(?:e-?\d+)?/g) ?? [];
  let i = 0;
  let mode = "";
  while (i < tokens.length) {
    const t = tokens[i];
    if (t === "M" || t === "L") {
      mode = t;
      i++;
      continue;
    }
    const x = Math.round(Number(tokens[i]) * 10) / 10;
    const y = Math.round(Number(tokens[i + 1]) * 10) / 10;
    i += 2;
    if (mode === "M") {
      current = [x, y];
      lines.push(current);
      mode = "L"; // implicit lineto after moveto per SVG spec
    } else {
      current?.push(x, y);
    }
  }
  return lines.filter((l) => l.length >= 4);
}

const glyphs = {};
const glyphRe = /<glyph\s+([^>]*?)\/>/g;
let m;
while ((m = glyphRe.exec(svg)) !== null) {
  const attrs = m[1];
  const uni = attrs.match(/unicode="([^"]+)"/)?.[1];
  if (!uni) continue;
  const ch = decodeEntity(uni);
  if (!WANTED.has(ch)) continue;
  const adv = Number(attrs.match(/horiz-adv-x="([^"]+)"/)?.[1] ?? face.defaultAdv);
  const d = attrs.match(/d="([^"]*)"/)?.[1] ?? "";
  glyphs[ch] = { adv, lines: d ? parsePath(d) : [] };
}

const missing = [...WANTED].filter((c) => !(c in glyphs));
if (missing.length) console.warn("Missing glyphs:", JSON.stringify(missing.join("")));

const out = `// AUTO-GENERATED by scripts/gen-script-font.mjs — do not edit by hand.
// Single-stroke cursive font "EMS Allure" (Evil Mad Scientist plotter fonts,
// via the hersheytext package). Polylines in font units, y-up, baseline at 0.

export interface ScriptGlyph {
  /** Horizontal advance in font units. */
  adv: number;
  /** Pen polylines: flat [x0,y0, x1,y1, …] per continuous stroke. */
  lines: number[][];
}

export const SCRIPT_FACE = ${JSON.stringify(face)} as const;

export const SCRIPT_GLYPHS: Record<string, ScriptGlyph> = ${JSON.stringify(glyphs)};
`;

writeFileSync("src/ai/scriptFont.ts", out);
const size = out.length;
console.log(
  `Wrote src/ai/scriptFont.ts: ${Object.keys(glyphs).length} glyphs, ${(size / 1024).toFixed(0)} KB`
);
